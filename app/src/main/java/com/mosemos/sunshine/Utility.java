    public static String getPreferredLocation(Context context) {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        return prefs.getString(context.getString(R.string.pref_location_key),
                context.getString(R.string.pref_location_default));
    }


    public static String formatWind(Context context, float windSpeed, float degrees) {
        int windFormat;
        if (Utility.isMetric(context)) {
            windFormat = R.string.format_wind_kmh;
        } else {
            windFormat = R.string.format_wind_mph;
            windSpeed = .621371192237334f * windSpeed;
        }

        // From wind direction in degrees, determine compass direction as a string (e.g NW)
        // You know what's fun, writing really long if/else statements with tons of possible
        // conditions.  Seriously, try it!
        String direction = "Unknown";
        if (degrees >= 337.5 || degrees < 22.5) {
            direction = "N";
        } else if (degrees >= 22.5 && degrees < 67.5) {
            direction = "NE";
        } else if (degrees >= 67.5 && degrees < 112.5) {
            direction = "E";
        } else if (degrees >= 112.5 && degrees < 157.5) {
            direction = "SE";
        } else if (degrees >= 157.5 && degrees < 202.5) {
            direction = "S";
        } else if (degrees >= 202.5 && degrees < 247.5) {
            direction = "SW";
        } else if (degrees >= 247.5 && degrees < 292.5) {
            direction = "W";
        } else if (degrees >= 292.5 && degrees < 337.5) {
            direction = "NW";
        }
        return String.format(context.getString(windFormat), windSpeed, direction);
    }

    public static boolean isMetric(Context context) {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        return prefs.getString(context.getString(R.string.pref_tempUnit_key),
                context.getString(R.string.pref_tempUnit_metric))
                .equals(context.getString(R.string.pref_tempUnit_metric));
    }

    public static String formatTemperature(Context context, double temperature) {
        double temp;
        if ( !isMetric(context) ) {
            temp = 9*temperature/5+32;
        } else {
            temp = temperature;
        }
        return context.getString(R.string.format_temp, temp);
    }

    static String formatDate(String dateStr) {
        SimpleDateFormat dbSimpleDateFormat = new SimpleDateFormat("yyyyMMdd");

        String formattedDateString = null;
        try {
            Date dbDate = adjustDate(dbSimpleDateFormat.parse(dateStr));
            GregorianCalendar gc = (GregorianCalendar) GregorianCalendar.getInstance();

            // if date is today: Today, MMM dd
            if(dbDate.equals(adjustDate(gc.getTime()))){
                formattedDateString = "Today, " + (new SimpleDateFormat("MMMM dd").format(dbDate));
            }
            else{
                gc.add(GregorianCalendar.DAY_OF_YEAR, 1);

                // if date is tomorrow: TOMORROW
                if(dbDate.equals(adjustDate(gc.getTime()))) {
                    formattedDateString = "Tomorrow";
                }
                else{
                    gc.add(GregorianCalendar.DAY_OF_YEAR, 6);
                    // if date is in this week: WEEKDAY
                    if(dbDate.before(adjustDate(gc.getTime()))){
                        formattedDateString = new SimpleDateFormat("EEEE").format(dbDate);
                    }
                    // else: EEE, MMM dd
                    else{
                        formattedDateString = new SimpleDateFormat("EEE, MMM dd").format(dbDate);
                    }
                }
            }
        } catch (ParseException e) {
            Log.e("Utility", "Cannot convert date: " + dateStr);
            e.printStackTrace();
        }

        return formattedDateString;
    }

    private static Date adjustDate(Date date){
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(date);
        calendar.set(Calendar.HOUR_OF_DAY, 0);
        calendar.set(Calendar.MINUTE, 0);
        calendar.set(Calendar.SECOND, 0);
        calendar.set(Calendar.MILLISECOND, 0);

        return calendar.getTime();
    }

    /**
     * Helper method to provide the icon resource id according to the weather condition id returned
     * by the OpenWeatherMap call.
     * @param weatherId from OpenWeatherMap API response
     * @return resource id for the corresponding icon. -1 if no relation is found.
     */
    public static int getIconResourceForWeatherCondition(int weatherId) {
        // Based on weather code data found at:
        // http://openweathermap.org/weather-conditions
        if (weatherId >= 200 && weatherId <= 232) {
            return R.drawable.ic_storm;
        } else if (weatherId >= 300 && weatherId <= 321) {
            return R.drawable.ic_light_rain;
        } else if (weatherId >= 500 && weatherId <= 504) {
            return R.drawable.ic_rain;
        } else if (weatherId == 511) {
            return R.drawable.ic_snow;
        } else if (weatherId >= 520 && weatherId <= 531) {
            return R.drawable.ic_rain;
        } else if (weatherId >= 600 && weatherId <= 622) {
            return R.drawable.ic_snow;
        } else if (weatherId >= 701 && weatherId <= 761) {
            return R.drawable.ic_fog;
        } else if (weatherId == 761 || weatherId == 781) {
            return R.drawable.ic_storm;
        } else if (weatherId == 800) {
            return R.drawable.ic_clear;
        } else if (weatherId == 801) {
            return R.drawable.ic_light_clouds;
        } else if (weatherId >= 802 && weatherId <= 804) {
            return R.drawable.ic_cloudy;
        }
        return -1;
    }

